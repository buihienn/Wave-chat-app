    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String username = jTextUsername.getText().trim();
        String fullName = jTextFullName.getText().trim();
        String address = jTextAddress.getText().trim();
        String gender = jTextGender.getText().trim();
        
        UserBUS userBUS = new UserBUS();
        if (userBUS.updateUser(username, fullName, address, gender)) {
            JOptionPane.showMessageDialog(this, "User updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            // Hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Failed to update user. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitActionPerformed

